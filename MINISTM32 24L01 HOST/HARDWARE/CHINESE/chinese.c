void show_chinese16(u16 x0,u16 y0,u8 size,u8 *p)
{
	u8 k=0,i,j,xoffset=0,color;
	u8 wordNum;//字库字数
	u8 x=x0,y=y0;
	//0`2 3`5 6`8
	while(p[k]!='\0'){
		for(wordNum=0;wordNum<20;wordNum++)
	{
		if((p[k]==code_GB_16[wordNum].Index[0])&&(p[k+1]==code_GB_16[wordNum].Index[1])){
				x0=x+xoffset*16;
				y0=y;
				for(i=0;i<32;i++){
					if(i%2==0){
						x0=x+xoffset*16;
						y0++;
					}
				color=code_GB_16[wordNum].Msk[i];
				for(j=0;j<8;j++){
						if((color&0x01)==0x01){
							LCD_Fast_DrawPoint(x0++,y0,BLACK);
						}else
						{
							LCD_Fast_DrawPoint(x0++,y0,WHITE);
						}
						color=color>>1;
					}
				}
				break;
			}
		}
	k+=2;
	xoffset++;
	}
}

